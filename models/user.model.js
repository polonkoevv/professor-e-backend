class User{

    constructor(first_name, last_name, email, phonenumber, password){
        this.email = email
        this.first_name = first_name
        this.last_name = last_name
        this.phonenumber = phonenumber
        this.password = password
    }

    first_name
    last_name
    email
    phonenumber
    password
}

export default User